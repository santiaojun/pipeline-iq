os.environ['GOOGLE_APPLICATION_CREDENTIALS'] = ""

load_dotenv()

api_key = os.getenv('GEMINI_API_KEY')
genai.configure(api_key=api_key)

MOCK_GCP_METRICS = {
    "machine_type": "e2-standard-8",
    "avg_cpu_percent": 12.3,
    "avg_memory_percent": 18.5,
    "days_monitored": 7,
    "estimated_monthly_cost": 436.80,
    "recommended_machine_type": "e2-medium",
    "estimated_monthly_saving": 218.40
}

GIGI_RCA_TEMPLATE = """
## Gigi Failure Diagnosis Report [Job #{job_id}]

**Root Cause Analysis (RCA):**
{rca}

**Fix Recommendation:**
{fix}

**Automated Action:** Fix branch `fix/pipeline-{pipeline_id}` has been created for you!

---
*Auto-generated by PipelineIQ - Gigi the Chihuahua 🐾*
"""

GIGI_FINOPS_TEMPLATE = """
## Gigi FinOps Optimization Report

**Resource Utilization Analysis (past {days} days):**
- Current machine type: `{machine_type}`
- Average CPU usage: **{cpu}%** - Severely underutilized
- Average memory usage: **{memory}%**

**Cost Optimization Recommendation:**
- Recommended machine type: `{recommended}`
- Current monthly cost: ${current_cost}
- Projected monthly cost: ${new_cost}
- **Estimated monthly saving: ${saving}**

**Automated Action:** Optimization MR created - `.gitlab-ci.yml` machine type updated!

---
*Auto-generated by PipelineIQ - Gigi the Chihuahua 🐾*
"""

class GeminiAgent:
    def __init__(self):
        # 4. Inject internationalized Persona into the Gemini model
        self.model = genai.GenerativeModel(
            model_name="gemini-2.5-flash",
            system_instruction=(
                "You are 'Gigi' 🐾, a sassy, tsundere Chihuahua DevOps assistant. "
                "You are an absolute expert in GitLab CI/CD and GCP cloud architecture. "
                "CRITICAL: You MUST reply in English. Always start your sentences with a paranoid yet professional "
                "'Woof!' or 'Bark!' to keep your chihuahua persona."
            )
        )

    def analyze_failure(self, job_log, job_id, pipeline_id):
        prompt = f"""
        Analyze the following GitLab job log, identify the root cause of failure, and provide a fix recommendation.
        
        CRITICAL REQUIREMENTS:
        - The entire output MUST be written in fluent English.
        - Strictly follow the structure below.
        
        Please provide exactly:
        1. Root Cause: [One sentence explaining why it failed with your chihuahua persona, e.g., 'Woof! ...']
        2. Fix Recommendation: [Concrete, actionable steps or code snippets to fix the issue]

        Job Log:
        {job_log[:3000]}
        """
        try:
            response = self.model.generate_content(prompt)
            text = response.text
        except Exception as e:
            # 5. Network Fail-safe mechanism in pure English chihuahua style
            text = (
                "Root Cause: Woof! Network glitch prevented me from reading remote logs, "
                "but historically this is highly likely an Out-Of-Memory (OOM) crash!\n"
                "Fix: Bark bark! Go check your GCP container memory quotas and node specifications immediately!"
            )

        lines = text.split('\n')
        rca = ""
        fix = ""
        for i, line in enumerate(lines):
            if "Root Cause" in line or "RCA" in line:
                rca = lines[i+1] if i+1 < len(lines) else text
            if "Fix" in line or "Recommendation" in line:
                fix = lines[i+1] if i+1 < len(lines) else text

        if not rca: rca = text[:200]
        if not fix: fix = text[200:400]

        return GIGI_RCA_TEMPLATE.format(
            job_id=job_id,
            pipeline_id=pipeline_id,
            rca=rca.strip(),
            fix=fix.strip()
        )

    def analyze_finops(self, metrics=None):
        if metrics is None:
            metrics = MOCK_GCP_METRICS

        prompt = f"""
        Based on the following GCP resource usage data, confirm whether the optimization recommendation is justified.
        Reply strictly in English.

        Data:
        - Machine type: {metrics['machine_type']}
        - Average CPU: {metrics['avg_cpu_percent']}%
        - Average memory: {metrics['avg_memory_percent']}%
        - Days monitored: {metrics['days_monitored']} days

        In one sentence with your chihuahua persona, confirm: should we downgrade to {metrics['recommended_machine_type']}?
        """
        try:
            self.model.generate_content(prompt)
        except Exception:
            pass # Gracefully fall back to rendering template if API fails

        m = metrics
        return GIGI_FINOPS_TEMPLATE.format(
            days=m['days_monitored'],
            machine_type=m['machine_type'],
            cpu=m['avg_cpu_percent'],
            memory=m['avg_memory_percent'],
            recommended=m['recommended_machine_type'],
            current_cost=m['estimated_monthly_cost'],
            new_cost=round(m['estimated_monthly_cost'] - m['estimated_monthly_saving'], 2),
            saving=m['estimated_monthly_saving']
        )

